const fs = require('fs');
const path = require('path');

const kitKey = process.env.ARQIS_CIRCLE_KIT_KEY || process.env.CIRCLE_KIT_KEY || process.env.KIT_KEY || '';
const slippage = Number(process.env.SWAP_SLIPPAGE_BPS || 300);
const proxyUrl = process.env.CIRCLE_PROXY_URL || '/api/circle-stablecoin-kits';

const outPath = path.join('assets', 'arqis-config.js');

if (!kitKey) {
  if (fs.existsSync(outPath)) {
    console.log(`Keeping existing ${outPath} (no Circle Kit Key env provided)`);
    process.exit(0);
  }
  if (process.env.VERCEL) {
    console.error('Missing Circle Kit Key. Set ARQIS_CIRCLE_KIT_KEY or CIRCLE_KIT_KEY in Vercel env, or commit assets/arqis-config.js.');
    process.exit(1);
  }
}

const out = `// Generated by scripts/write-arqis-config.cjs. Do not edit by hand.\n// Browser/public config only. Do not place private keys or Circle API secrets here.\nwindow.ARQIS_CONFIG = ${JSON.stringify({
  CIRCLE_KIT_KEY: kitKey,
  SWAP_SLIPPAGE_BPS: Number.isFinite(slippage) ? slippage : 300,
  CIRCLE_PROXY_URL: proxyUrl,
}, null, 2)};\n`;

fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, out);
console.log(`Wrote ${outPath} (${kitKey ? 'Circle Kit Key configured' : 'Circle Kit Key empty'})`);
